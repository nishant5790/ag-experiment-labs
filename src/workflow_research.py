#-------------------
# Create agents
#-------------------

hackernews_agent= Agent(
    name="hackernews_agent",
    model=Gemini(
        id="gemini-2.5-flash"
    ),
    tools= [HackerNewsTools()],
    role = "Extract key insights and content from HackerNews post",
)

web_agent= Agent(
    name="Web agent",
    model=Gemini(
        id="gemini-2.5-flash"
    ),
    tools= [WebSearchTools()],
    role = "Search the web for the latest news and trends",
)

content_planner= Agent(
    name="Content planner",
    model=Gemini(id="gemini-2.5-flash"),
    instructions=[
        "Plan a queries for the  topic and research content",
        "Ensure that I have detailed sources with relevant links",
        "only return the topic names in dict format [{category_name : [queries]}]"
    ]
)


writer_agent= Agent(
    name="Writer agent",
    model=Gemini(id="gemini-2.5-flash"),
    instructions="Write a detailed report with sources on the topic using planning ",
)


#---------------------
# Create Team
#---------------------

research_team = Team(

    name="Research team",
    model=Gemini(id="gemini-2.5-flash"),
    members=[hackernews_agent, web_agent],
    instructions="Research the topics from hackernews and the web using the queries and return the content with source and links",
    stream_member_events=True,
    show_members_responses=True
)

#--------------------
# Define steps
#--------------------

research_step = Step(
    name="Research step",
    team=research_team,
)

content_planning_step = Step(
    name="Content planning step",
    agent=content_planner,
)

async def prepare_input_for_web_search(step_input: StepInput,)->AsyncIterator[StepOutput]:
    topic =step_input.input
    planning_output = step_input.previous_step_outputs
    content= dedent(
        f"""
        I am writing a research report on the topic
        <topic>
        {topic}
        </topic>
        using the queries generated by planning_output
        <planned query>
        {planning_output}
         </planning_queries>
        Search the web for most credible articles with source 
        """
    )
    yield StepOutput(content=content)


async def prepare_input_for_writer(step_input: StepInput, ) -> AsyncIterator[StepOutput]:
    topic = step_input.input
    research_team_output = step_input.previous_step_outputs
    content = dedent(
        f"""
        I am writing a research report on the topic
        <topic>
        {topic}
        </topic>
        
        Here is information from from the web:
        <research_result>
        {research_team_output}
        </research_result>
        """
    )
    yield StepOutput(content=content)

#----------------------------
# Create Workflow
#----------------------------

content_creation_workflow = Workflow(
    name="Content creation workflow",
    description="Automated content creation from ",
    db=SqliteDb(
        session_table="workflow_session",
        db_file = "tmp/workflow.db",
    ),
    steps=[ content_planning_step,prepare_input_for_web_search,research_step,prepare_input_for_writer,writer_agent],
)

